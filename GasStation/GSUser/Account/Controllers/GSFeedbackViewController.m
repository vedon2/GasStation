                      @"other" : jobsImage
                      };
    JSQMessagesBubbleImageFactory *bubbleFactory = [[JSQMessagesBubbleImageFactory alloc] init];
    self.outgoingBubbleImageData = [bubbleFactory outgoingMessagesBubbleImageWithColor:[UIColor jsq_messageBubbleLightGrayColor]];
    self.incomingBubbleImageData = [bubbleFactory incomingMessagesBubbleImageWithColor:[UIColor jsq_messageBubbleGreenColor]];
    
    UIButton *voiceButton = [UIButton buttonWithType:UIButtonTypeCustom];
    [voiceButton setImage:[UIImage imageNamed:@"icon_other_voice"] forState:UIControlStateNormal];
    [voiceButton setImage:[UIImage imageNamed:@"icon_other_write"] forState:UIControlStateSelected];
    self.inputToolbar.contentView.leftBarButtonItem = voiceButton;
//    UIButton *sendButton = [UIButton buttonWithType:UIButtonTypeCustom];
//    self.inputToolbar.contentView.rightBarButtonItem = sendButton;
    self.inputToolbar.contentView.textView.returnKeyType = UIReturnKeyDone;
    
    
    
    if (!_voiceMessageButton)
    {
        [self.inputToolbar.contentView addSubview:self.voiceMessageButton];
        [self.voiceMessageButton autoPinEdge:ALEdgeTop toEdge:ALEdgeTop ofView:self.inputToolbar.contentView.textView];
        [self.voiceMessageButton autoPinEdge:ALEdgeRight toEdge:ALEdgeRight ofView:self.inputToolbar.contentView.textView];
        [self.voiceMessageButton autoPinEdge:ALEdgeBottom toEdge:ALEdgeBottom ofView:self.inputToolbar.contentView.textView];
        [self.voiceMessageButton autoPinEdge:ALEdgeLeft toEdge:ALEdgeLeft ofView:self.inputToolbar.contentView.textView];
